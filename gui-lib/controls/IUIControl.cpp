
#include "IUIControl.h"

namespace gui
{
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	IUIControl::IUIControl(uint16_t x, uint16_t y, uint16_t w, uint16_t h, const IUIContext & renderer)
		:X{x}, Y{y}, Width{w}, Height{h}, _enable{true}, _visible{true}, _renderer{renderer}
	{
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	void IUIControl::OnEncoderMoved(EncoderEvent & action)
	{
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	void IUIControl::Draw()
	{
		GetGraphicElement()->DrawWithRenderer(X, Y, _context->Renderer);
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	bool IUIControl::IsEnable() const
	{
		return _enable;
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	void IUIControl::SetEnable(bool enable)
	{
		_enable = enable;
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	bool IUIControl::IsVisible() const
	{
		return _visible;
	}
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	void IUIControl::SetVisible(bool visible)
	{
		_visible = visible;
	}
	
	/*--------------------------------------------------------------------------//
	// ------------------------- Protected Methods -----------------------------
	//--------------------------------------------------------------------------*/
	
	/*--------------------------------------------------------------------------//
	//
	//--------------------------------------------------------------------------*/
	bool IUIControl::IsPositionInsideControl(uint16_t x, uint16_t y) const
	{
		return ((X < x)&&((X + Width) > x))&&((Y < y)&&((Y + Height) > y));
	}
}